raio = float(input(''))

pi = 3.141592
cc = 2 * pi * raio
ac = pi * raio ** 2
ae = 4 * pi * raio ** 2
ve = 4 / 3 * pi * raio ** 3
# cc = comprimento da circunferência
# ac = área da circunferência
# ae = área da esfera
# ve = volume da esfera

print(f'{cc:.6f}')
print(f'{ac:.6f}')
print(f'{ae:.6f}')
print(f'{ve:.6f}')
